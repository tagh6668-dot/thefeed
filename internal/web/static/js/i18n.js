// Order: native bridge (iOS/Android) → localStorage → 'fa'.
// iOS localStorage is per-origin (host:port) so picking up the
// native value first survives an embedded server rebinding to a
// new loopback port on each launch.
var lang = (function () {
  // Same precedence as the modal-init script: server first (so a
  // wiped thefeeddata/ also wipes the language), then native bridge,
  // then localStorage as a final cache.
  try {
    var xhr = new XMLHttpRequest();
    xhr.open('GET', '/api/settings', false);
    xhr.send();
    if (xhr.status === 200) {
      var d = JSON.parse(xhr.responseText);
      if (d.lang) return d.lang;
    }
  } catch (e) { }
  try { if (typeof IOS !== 'undefined' && IOS.getLang) { var v = IOS.getLang(); if (v) return v; } } catch (e) { }
  try { if (typeof Android !== 'undefined' && Android.getLang) { var v2 = Android.getLang(); if (v2) return v2; } } catch (e) { }
  return localStorage.getItem('thefeed_lang') || '';
})();
function t(k) { return (I18N[lang] && I18N[lang][k]) || I18N.en[k] || k }
function applyLang() {
  var isRtl = lang === 'fa';
  document.documentElement.dir = isRtl ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(function (el) { el.textContent = t(el.dataset.i18n) });
  document.querySelectorAll('[data-i18n-ph]').forEach(function (el) { el.placeholder = t(el.dataset.i18nPh) });
  document.querySelectorAll('[data-i18n-title]').forEach(function (el) { el.title = t(el.dataset.i18nTitle) });
  document.querySelectorAll('[data-i18n-body]').forEach(function (el) { el.innerHTML = t(el.dataset.i18nBody) });
  var lf = document.getElementById('langFa'); if (lf) lf.classList.toggle('active-lang', lang === 'fa');
  var le = document.getElementById('langEn'); if (le) le.classList.toggle('active-lang', lang === 'en');
  // Null-guarded: the send input is absent while a conversation is closed or
  // mid-transition. Without the guard a missing #sendInput throws here and
  // aborts the whole language switch — the "language selection broke" class.
  var _si = document.getElementById('sendInput'); if (_si) _si.style.direction = isRtl ? 'rtl' : 'ltr';
  applyThemeButtons();
  // Re-render dynamic content. Wrapped so a throw in any one renderer can't
  // abort the language switch that already applied above.
  try {
    if (channels.length > 0) renderChannels();
    // Profile button label: shows the active profile name, or a translated
    // "Set Up" when none exist (the span has no data-i18n by design).
    if (typeof renderProfileBtn === 'function') renderProfileBtn();
    // Re-render views so RTL/LTR layout updates take effect immediately
    if (typeof renderSavedView === 'function') {
      var _msgsEl = document.getElementById('messages');
      if (_msgsEl && _msgsEl.classList.contains('saved-mode')) renderSavedView();
    }
    if (typeof tmRenderChannels === 'function') tmRenderChannels();
    if (typeof renderNavBar === 'function') renderNavBar();
  } catch (e) { }
}
function setLang(l) {
  lang = l;
  localStorage.setItem('thefeed_lang', l);
  applyLang();
  if (typeof Android !== 'undefined') try { Android.setLang(l) } catch (e) { }
  if (typeof IOS !== 'undefined' && IOS.setLang) try { IOS.setLang(l) } catch (e) { }
  fetch('/api/settings', { method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify({ lang: l }) }).catch(function () { });
}

